def select_dependencies(namespace, keys):
    missing = [key for key in keys if key not in namespace]
    if missing:
        missing_text = ", ".join(sorted(missing))
        raise KeyError(f"Missing dependencies: {missing_text}")
    return {key: namespace[key] for key in keys}
